package tugas03_lingkaran;

/**
 *
 * @author Fajar Sanggi Peratama
 */
public class Lingkaran {
    public int x;
    public int y;
    public int r;

    public Lingkaran() {
}

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }
    
}
